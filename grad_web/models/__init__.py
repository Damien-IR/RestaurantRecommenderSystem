from django.db import models


class Account(models.Model):
    id = models.AutoField(primary_key=True)
    email = models.TextField()
    password = models.TextField()

    class Meta:
        db_table = "account"


class Rest(models.Model):
    id = models.IntegerField(primary_key=True)
    xPos = models.FloatField()
    yPos = models.FloatField()
    name = models.CharField(max_length=100)
    blogCafeReviewCount = models.IntegerField()
    bookingReviewCount = models.IntegerField()
    hasBooking = models.BooleanField()
    hasNpay = models.BooleanField()
    category = models.CharField(max_length=45)
    phone = models.CharField(max_length=45)
    roadAddr = models.CharField(max_length=100)
    commonAddr = models.CharField(max_length=100)
    businessCategory = models.CharField(max_length=30)
    imageSrc = models.CharField(max_length=500)
    priceCategory = models.CharField(max_length=100)
    options = models.CharField(max_length=100)

    class Meta:
        db_table = "rest"


class Rating(models.Model):
    index = models.AutoField(primary_key=True)
    custid = models.IntegerField()
    restid = models.IntegerField()
    rating = models.IntegerField()

    class Meta:
        db_table = "rating"


